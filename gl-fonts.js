var ColorFromString = function ColorFromString(r, i) {
    i = i || 0;
    var g, b, a = 1;
    if (r[0 + i] != '^') {
        return;
    }
    var len
    if (r[1 + i] == 'x') {
        b = parseInt(r.slice(6 + i, 8 + i), 16) / 255;
        g = parseInt(r.slice(4 + i, 6 + i), 16) / 255;
        r = parseInt(r.slice(2 + i, 4 + i), 16) / 255;
        len = 8;
    }
    else {
        var obj = colorLookup[parseInt(r.slice(1 + i, 2 + i))];
        obj.len = 2;
        return obj;
    }

    return {
        r: r,
        g: g,
        b: b,
        a: a,
        len: len
    }
}

var StringToFormattedArray = function StringToFormattedArray(str) {
    var ret = [];

    var last_find = 0;

    for (var i = 0; i < str.length; i++) {
        var col = this.ColorFromString(str, i);
        if (col) {
            if (last_find != i) {
                ret.push(str.slice(last_find, i));
            }
            ret.push(col);
            last_find = (i += col.len);
        }
    }

    if (i != last_find) {
        ret.push(str.slice(last_find, i));
    }

    return ret;
}

var loadFont = function loadFont(gl, font)
{
	return new Promise( (resolve, reject) => {
		var img = document.createElement("img");

		font.Texture = gl.createTexture();

		img.onload = () => {
			gl.bindTexture(gl.TEXTURE_2D, font.Texture);
			gl.texImage2D(gl.TEXTURE_2D, 0, gl.RGBA, gl.RGBA, gl.UNSIGNED_BYTE, img);

			// Might not be power-of-2. We don't care for now, but this might hurt us
			gl.texParameteri(gl.TEXTURE_2D, gl.TEXTURE_WRAP_S, gl.CLAMP_TO_EDGE);
			gl.texParameteri(gl.TEXTURE_2D, gl.TEXTURE_WRAP_T, gl.CLAMP_TO_EDGE);
			gl.texParameteri(gl.TEXTURE_2D, gl.TEXTURE_MIN_FILTER, gl.LINEAR);

			font.TextureWidth = img.width;
			font.TextureHeight = img.height;
			resolve();
		};

		img.onerror = () => {
			gl.bindTexture(gl.TEXTURE_2D, font.Texture);

			var pinkRGBA = new Uint8Array([255, 0, 220, 255]);
			gl.texImage2D(gl.TEXTURE_2D, 0, gl.RGBA, 1, 1, 0, gl.RGBA, gl.UNSIGNED_BYTE, pinkRGBA);

			font.TextureWidth = 1;
			font.TextureHeight = 1;
			resolve();
		};

		img.src = font.SpriteMap;
	} );
};

var getGlyphs = function getGlyphs(fontName, height)
{
	var x, y;

	if ( !(x = FONTS[fontName]) )
		return null;

	if ( !(y = x[height]) )
		return null;

	return y.Glyphs;
}

//
// Loads all the data necessary to render fonts
// Returns a promise
//
glFonts.Load = function Load(gl)
{
	var fontLoaders = [];

	// WOW change this
	for (var font in FONTS)
	{
		if (!FONTS.hasOwnProperty(font))
			continue;

		for (var innerFont in FONTS[font])
		{
			if (!FONTS[font].hasOwnProperty(innerFont))
				continue;

			fontLoaders.push(loadFont(gl, FONTS[font][innerFont]));
		}
	}

	return Promise.all(fontLoaders);
}

glFonts.GetTextWidth = function GetTextWidth(fontName, height, text)
{
	if (fontName = "VAR BOLD") fontName = "VAR";

	var glyphs = getGlyphs(fontName, height);
	var width = 0;

	for (var i = 0; i < text.length; i++)
	{
		var glyph = glyphs[text.charCodeAt(i)];

		if (!glyph)
			glyph = glyphs[0];

		width += glyph[2] + glyph[3] + glyph[4]; // TODO: Do we need to cut off glyph[4] for the last character?
	}

	return width;
}

//
// Returns arrays of vertex-positions & texture-coordinates needed to render the given text
//
glFonts.BuildBuffers = function GetTextBuffers(fontName, height, text, baseX, baseY)
{
	// Looks SHIT!
	baseX = Math.round( baseX );
	baseY = Math.round( baseY );

	if (fontName = "VAR BOLD") fontName = "VAR";

	var font = FONTS[fontName][height];
	var glyphs = getGlyphs(fontName, height);

	var positions = new Float32Array(text.length * 12);
	var texCoords = new Float32Array(text.length * 12);

	var baseX = baseX;

	var parts = StringToFormattedArray(text);

	for (var i = 0; i < parts.length; i++)
	{
		var part = parts[i];

		if (typeof part != "string")
			continue;

		for (var j = 0; j < part.length; j++)
		{
			var glyph = glyphs[part.charCodeAt(j)];

			if (!glyph)
				glyph = glyphs[0];

			baseX += glyph[3];

			var pxTL = baseX;
			var pyTL = baseY;
			var pxTR = baseX + glyph[2];
			var pyTR = baseY;
			var pxBR = baseX + glyph[2];
			var pyBR = baseY + height;
			var pxBL = baseX;
			var pyBL = baseY + height;

			var txTL = glyph[0] / font.TextureWidth;
			var tyTL = glyph[1] / font.TextureHeight;
			var txTR = (glyph[0] + glyph[2]) / font.TextureWidth;
			var tyTR = glyph[1] / font.TextureHeight;
			var txBR = (glyph[0] + glyph[2]) / font.TextureWidth;
			var tyBR = (glyph[1] + height) / font.TextureHeight;
			var txBL = glyph[0] / font.TextureWidth;
			var tyBL = (glyph[1] + height) / font.TextureHeight;

			/// Triangle #1
			// TL
			positions[j * 12 + 0] = pxTL;
			positions[j * 12 + 1] = pyTL;
			texCoords[j * 12 + 0] = txTL;
			texCoords[j * 12 + 1] = tyTL;

			// BL
			positions[j * 12 + 2] = pxBL;
			positions[j * 12 + 3] = pyBL;
			texCoords[j * 12 + 2] = txBL;
			texCoords[j * 12 + 3] = tyBL;

			// TR
			positions[j * 12 + 4] = pxTR;
			positions[j * 12 + 5] = pyTR;
			texCoords[j * 12 + 4] = txTR;
			texCoords[j * 12 + 5] = tyTR;

			/// Triangle #2
			// TR
			positions[j * 12 + 6] = pxTR;
			positions[j * 12 + 7] = pyTR;
			texCoords[j * 12 + 6] = txTR;
			texCoords[j * 12 + 7] = tyTR;

			// BL
			positions[j * 12 + 8] = pxBL;
			positions[j * 12 + 9] = pyBL;
			texCoords[j * 12 + 8] = txBL;
			texCoords[j * 12 + 9] = tyBL;

			// BR
			positions[j * 12 + 10] = pxBR;
			positions[j * 12 + 11] = pyBR;
			texCoords[j * 12 + 10] = txBR;
			texCoords[j * 12 + 11] = tyBR;

			baseX += glyph[2] + glyph[4];
		}
	}

	return { Texture: FONTS[fontName][height].Texture, Positions: positions, TexCoords: texCoords };
}

